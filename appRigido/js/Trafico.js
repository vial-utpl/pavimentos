function CalcularTrafico() {
    var pt = $('#trf_pt').val() * 1;
    var espesor = $('#trf_espesor').val() * 1;

    $(listaVehiculos).each(function (i, item) {
        CalcularTraficoItem(item, pt, espesor);
    });
}

